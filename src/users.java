    private void savebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_savebtnMouseClicked
        // TODO add your handling code here:
        if(id.getText().isEmpty() || username.getText().isEmpty() || password.getText().isEmpty() || telephone.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No User to Save");
        }else {
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/curelinkdb", "root", "");
                PreparedStatement add = Con.prepareStatement("Insert into userstable values(?,?,?,?)");
                add.setString(1, id.getText());
                add.setString(2, username.getText());
                add.setString(3, password.getText());
                add.setString(4, telephone.getText());
                
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "User Saved");
                DisplayUser();
                Reset();
            }catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_savebtnMouseClicked

